public class asg2 {
    public static void main(String[] args) {
        int a = 10;
        short b = 20;
        long c = 1000000L;
        byte d = 5;

        System.out.println("int a = " + a);
        System.out.println("short b = " + b);
        System.out.println("long c = " + c);
        System.out.println("byte d = " + d);
    }
}